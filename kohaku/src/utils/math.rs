use std::cmp;
//----------------------------------------------------------------------------------------------------------------------

pub fn eq_f(value: f32, other: f32) -> bool {
    (value - other).abs() <= 1e-5
}
//----------------------------------------------------------------------------------------------------------------------

// TODO replace with https://github.com/rust-lang/rust/pull/77872
pub fn clamp_f(value: f32, min: f32, max: f32) -> f32 {
    value.max(min).min(max)
}
//----------------------------------------------------------------------------------------------------------------------

pub fn clamp<T: std::cmp::Ord>(value: T, min: T, max: T) -> T {
    cmp::min(max, cmp::max(value, min))
}
